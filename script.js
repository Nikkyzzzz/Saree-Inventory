// Display sarees in cards
function displaySarees(items) {
    const container = document.getElementById('inventory');
    container.innerHTML = '';
    items.forEach(saree => {
        const card = document.createElement('div');
        card.classList.add('card');

        card.innerHTML = `
            <img src="${saree.Photo}" alt="Saree Image">
            <div class="card-content">
                <h3>${saree.Number}</h3>
                <p><strong>Color:</strong> ${saree.Color}</p>
                <p><strong>Fabric:</strong> ${saree.Fabric}</p>
                <p><strong>Price:</strong> ${saree["Cost Price"]}</p>
            </div>
        `;
        container.appendChild(card);
    });
}

